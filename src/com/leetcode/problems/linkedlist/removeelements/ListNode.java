package com.leetcode.problems.linkedlist.removeelements;

/**
 * @Author Duole
 * @Version 1.0
 */

class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

